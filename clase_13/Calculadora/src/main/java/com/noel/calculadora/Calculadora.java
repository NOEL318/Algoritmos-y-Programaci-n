    private void jbcsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbcsActionPerformed
        // TODO add your handling code here:
         current_Value = barra_numeral.getText();
         
         if(current_Value.contains("-")){
              barra_numeral.setText(current_Value.replace("-", "+"));
         }else if(current_Value.contains("+")){
               barra_numeral.setText(current_Value.replace("+", "-"));
         }else{
               barra_numeral.setText("+"+current_Value);
         }
         
    }//GEN-LAST:event_jbcsActionPerformed

