    private void jb6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb6ActionPerformed
        // TODO add your handling code here:
        current_Value = barra_numeral.getText();
        new_Value = current_Value + "6";
        barra_numeral.setText(new_Value);
        if (selected_operation.equals("suma")) {
            acum = acum + Double.valueOf(new_Value);
        } else if (selected_operation.equals("resta")) {
            acum = acum - last;
            acum = last - Double.valueOf(new_Value);
        } else if (selected_operation.equals("prod")) {
            acum = last * Double.valueOf(new_Value);
        }
         else if (selected_operation.equals("div")) {
            acum = last / Double.valueOf(new_Value);
        }
    }//GEN-LAST:event_jb6ActionPerformed

